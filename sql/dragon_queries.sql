#SUMMER REGIONS QUERY
SELECT A.NAME DRAGON, B.NAME REGION, C.DIET, D.WINGSPAN, E.CLASS, F.COLOUR, G.FIRE, G.NOTES, SDO_GEOM.SDO_CENTROID(B.SHAPE,0.005).SDO_POINT.X X,SDO_GEOM.SDO_CENTROID(B.SHAPE,0.005).SDO_POINT.Y Y
FROM S1217815.DRAGONS A, S1234874.REGION B, S1217815.DIET C, S1217815.WINGSPAN D, S1217815.WEIGHT_CLASS E, S1217815.COLOUR F, S1217815.DRAGON_TYPE G
WHERE A.TYPE_ID = G.TYPE_ID AND B.REGION_ID = A.REGION_SUMMER_ID AND G.DIET_ID = C.DIET_ID
AND D.WINGSPAN_ID = G.WINGSPAN_ID AND E.WEIGHT_ID = G.WEIGHT_ID AND F.COLOUR_ID = G.COLOUR_ID; 
